#!/usr/bin/env node
/**
 * Post-build PurgeCSS step using the PurgeCSS Node API.
 * For each unique CSS file in dist/assets, purge unused rules
 * against ALL HTML pages that reference it.
 *
 * Usage: node scripts/purge-build-css.mjs
 * Run after `astro build`. Modifies dist/assets CSS files in-place.
 */

import { readFile, writeFile, readdir, stat } from 'node:fs/promises';
import { join, basename } from 'node:path';
import { existsSync, mkdirSync } from 'node:fs';
import { PurgeCSS } from 'purgecss';

const ROOT = join(import.meta.dirname, '..');
const DIST = join(ROOT, 'dist');

const SAFELIST = [
  // Animations (JS-added)
  /^fadeIn/, /^slideIn/, /^zoomIn/, /^zoomOut/,
  /^elementor-animation/,
  /elementor-invisible/, /elementor-animated/, /animated/,

  // Navigation states (JS-added)
  /is-open/, /is-active/, /e-active/, /active/,
  /e-n-menu-title-text/, /e-n-menu-dropdown-icon/,

  // Carousel states (JS-added)
  /swiper-slide-active/, /swiper-slide-next/, /swiper-slide-prev/,
  /swiper-pagination-bullet-active/, /swiper-initialized/,
  /swiper-locked/, /swiper-button-disabled/,

  // Sticky header (JS-added)
  /elementor-sticky/,

  // Load more (JS-added)
  /e-load-more/,

  // Fit height (JS-added)
  /elementor-fit-height/,

  // Dialog states (JS-added)
  /home-dialog/,

  // Elementor dynamic
  /elementor-heading-title/, /elementor-button-content-wrapper/,
  /elementor-screen-only/, /elementor-hidden-/, /elementor-active/,
  /elementor-editor-active/,

  // WP image classes (generated by Astro components)
  /wp-image-/, /wp-post-image/, /attachment-/,

  // Size variants
  /elementor-size-/,
];

async function collectHtmlFiles(dir) {
  const results = [];
  const entries = await readdir(dir, { withFileTypes: true });
  for (const entry of entries) {
    const full = join(dir, entry.name);
    if (entry.isDirectory() && entry.name !== 'assets' && entry.name !== 'scripts' && !entry.name.startsWith('_') && entry.name !== 'cdn-cgi') {
      results.push(...(await collectHtmlFiles(full)));
    } else if (entry.name === 'index.html') {
      results.push(full);
    }
  }
  return results;
}

async function main() {
  console.log('PurgeCSS post-build optimization...\n');

  const allPages = await collectHtmlFiles(DIST);
  console.log(`Found ${allPages.length} HTML pages.`);

  // Map CSS file -> list of pages that reference it
  const cssPageMap = new Map();
  for (const page of allPages) {
    const html = await readFile(page, 'utf8');
    for (const m of html.matchAll(/href="(\/assets\/[^"]+\.css)"/g)) {
      const cssRelPath = m[1];
      const cssAbsPath = join(DIST, cssRelPath);
      if (!existsSync(cssAbsPath)) continue;
      if (!cssPageMap.has(cssAbsPath)) {
        cssPageMap.set(cssAbsPath, new Set());
      }
      cssPageMap.get(cssAbsPath).add(page);
    }
  }

  console.log(`Found ${cssPageMap.size} unique CSS files.\n`);

  // Load all HTML content into memory to avoid repeated reads
  const htmlCache = new Map();
  for (const page of allPages) {
    htmlCache.set(page, await readFile(page, 'utf8'));
  }

  let totalOriginal = 0;
  let totalPurged = 0;

  for (const [cssPath, pages] of cssPageMap) {
    const base = basename(cssPath);
    const originalSize = (await stat(cssPath)).size;
    totalOriginal += originalSize;

    if (base.startsWith('site-shell-local') || base.startsWith('home-local') || originalSize < 1000) {
      totalPurged += originalSize;
      continue;
    }

    // Gather all content that references this CSS
    const content = [];
    for (const page of pages) {
      const html = htmlCache.get(page) || (await readFile(page, 'utf8'));
      content.push({ raw: html, extension: 'html' });
    }

    const cssContent = await readFile(cssPath, 'utf8');

    try {
      const purgeResults = await new PurgeCSS().purge({
        css: [{ raw: cssContent }],
        content,
        safelist: SAFELIST,
        // Don't reject on missing fonts
        rejected: false,
        // Keep keyframes starting with our known animations
        keyframes: true,
        fontFace: true,
      });

      if (purgeResults.length > 0) {
        const purged = purgeResults[0].css;
        await writeFile(cssPath, purged);
        const purgedSize = Buffer.byteLength(purged);
        totalPurged += purgedSize;

        const saved = originalSize - purgedSize;
        const pct = ((saved / originalSize) * 100).toFixed(0);
        console.log(`  ${base}: ${(originalSize/1024).toFixed(0)}KB → ${(purgedSize/1024).toFixed(0)}KB (-${(saved/1024).toFixed(0)}KB, ${pct}%)`);
      } else {
        totalPurged += originalSize;
        console.log(`  ${base}: no output from PurgeCSS`);
      }
    } catch (e) {
      totalPurged += originalSize;
      console.warn(`  ${base}: PurgeCSS error — ${e.message.slice(0, 100)}`);
    }
  }

  const totalSaved = totalOriginal - totalPurged;
  console.log(`\nTotal: ${(totalOriginal/1024).toFixed(0)}KB → ${(totalPurged/1024).toFixed(0)}KB (saved ${(totalSaved/1024).toFixed(0)}KB, ${((totalSaved/totalOriginal)*100).toFixed(0)}%)`);
}

main().catch((e) => { console.error(e); process.exit(1); });
